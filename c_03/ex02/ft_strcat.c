char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
	{
		if (dest[i + 1] == '\0')
		{
			while (src[j] != '\0')
			{
				dest[i + j + 1] = src[j];
				j++;
			}
		}
		i++;
	}
	return (dest);
}
